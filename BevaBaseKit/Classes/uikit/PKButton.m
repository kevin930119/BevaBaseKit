#import "PKButton.h"

@implementation PKButton

- (instancetype)init {
    if (self = [super init]) {
        self.contentVerticalAlignment = UIControlContentVerticalAlignmentFill;
        self.contentHorizontalAlignment = UIControlContentHorizontalAlignmentFill;
    }
    return self;
}

@end
